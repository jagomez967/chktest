	create table #capacitados
	(
	cantidad int,
	--meses int, 
	cadena varchar(100), id int 
	)
	
	insert #capacitados (cadena, cantidad)
	select c.nombre,SUM(case when rmi.Valor4 not like '%[^0-9]%' then convert(int,rmi.Valor4) else 0 end)
	from MD_ReporteModuloItem rmi
	inner join #tempReporte r on r.IdReporte = rmi.IdReporte
	inner join MD_Item mi on mi.idItem = rmi.idItem
	inner join Cadena c on c.idCadena = r.id_Cadena
	inner join #usuario u on r.usuario = u.id_usuario
	where rmi.IdItem = 11227 
	and mi.idmodulo = 2442
	group by mi.nombre,c.nombre
   
	select cadena,cadena,cantidad, 1 showVal from #capacitados

	
	create table #datosRes
	(
		
		id_tipo int,
		tipo varchar(100),
		cadena varchar(100),
		cantidad int
		---meses int
	)
	
	insert #datosRes ( id_tipo,tipo,cantidad,cadena)
    
	select t.idTipo,t.nombre,
	SUM(case when rmi.Valor4 not like '%[^0-9]%' then convert(int,rmi.Valor4) else 0 end),c.nombre
	from MD_ReporteModuloItem rmi
	inner join #tempReporte r on r.IdReporte = rmi.IdReporte
	inner join MD_Item mi on mi.idItem = rmi.idItem
	inner join Cadena c on c.idCadena = r.id_Cadena
	inner join tipo t on r.tipo_pdv = t.idTipo
	where rmi.IdItem = 11227 
	and mi.idmodulo = 2442
	and r.tipo_pdv in (325,326)
	group by t.idTipo,t.nombre,c.nombre
	
	
	select cadena,cadena,id_tipo,tipo,cantidad, 1 showVal from #datosRes
	

   
