IF NOT EXISTS (SELECT * FROM sys.objects WHERE type = 'P' AND OBJECT_ID = OBJECT_ID('dbo.Whirpool_Services_Por_Categoria_T2'))
   exec('CREATE PROCEDURE [dbo].[Whirpool_Services_Por_Categoria_T2] AS BEGIN SET NOCOUNT ON; END')
